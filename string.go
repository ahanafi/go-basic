package main

import "fmt"

func main() {
	fmt.Println("Ahmad")
	fmt.Println("Ahmad Hanafi")

	fmt.Println("Length of 'Ahmad' is = ", len("Ahmad")) // Len => count length character of string
	fmt.Println("Ahmad Hanafi"[0])                       //Get first char at index 0, result is byte char
	fmt.Println("Ahmad Hanafi"[6])                       //Get first char at index 6, result is byte char
}
